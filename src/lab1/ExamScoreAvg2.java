package lab1;

public class ExamScoreAvg2 {

	public static void main(String[] args) { 
	
	// Initialize and declare exam scores.
	double examscore1 = 70.5;
	double examscore2 = 82.0;
	double examscore3 = 97.5,
				
	// Compute exam score average.
		examscoreavg = (examscore1 + examscore2 + examscore3) / 3;
		
	// Display the exam average.
		System.out.println("Exam AVG= " + examscoreavg);
		
	}
}